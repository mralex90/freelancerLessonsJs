// Планирование setTimeout & setInterval
/*
В Джаваскрипт существует возможность вызывать ф-ию не сразу, а с 
определенной задержкой. Называется это планирование вызова ф-ии.

Чтобы реализовать планирование существуют два метода:
1.  setTimeout позволяет вызвать функцию один раз через 
определенный интервал времени
2.  setInterval позволяет вызывать функцию регулярно, повторяя 
вызов через определенный интервал времени.
*/

