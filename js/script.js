/*
Такие методы планирования не гарантируют точную задержку, т.к. время 
зависит от многих факторов - загруженность процессора, Экономия батареи ноутбука и т.д.
 
Но, тем не менее заменить setInterval  мы можем на рекурсивный stTimeout.
Выглядит это следующим образом.
*/

function showMessage(text, name) {
    console.log(`${text}, ${name}`);
    setTimeout(showMessage, 500, text, name);
}

setTimeout(showMessage, 500, 'Hello', 'Oleksandr');
