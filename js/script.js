// Специальный обьект с методами для добавления/удаления одного класса.

// Получаем елемент
const element = document.querySelector('.lesson__item-list__red');

// Добавить класс
// element.classList.add('active');
// Удалить класс
// element.classList.remove('active');
// Добавить класс, если его нет, а если есть удалить
