// Вложенность и видимость функции

function getSum() {
    let numOne, numTwo;

    function getNumOne() {
        numOne = 1;
    }
    function getNumTwo() {
        numTwo = 2;
    }
    getNumOne();
    getNumTwo();

    let numSum = numOne + numTwo;
    console.log(numSum);
}

getSum();

/*
Функцию можно без проблем вкладывать во внутрь другой 
функции, запускать ее и все будет отлично работать.
Но попытка вызвать дочернюю ф-ию за пределами родительской
вызовет ошибку.
*/


