/*
Чтобы получить весь обьект или его значение мы просто можем вызвать 
переменную которой присвоен данный обьект.
Если мы хотим получить сразу весь обьект - мы просто обращаемся к 
этой переменной. Допустим выведя ее в консоль мы получим массив значений, 
где мы четко видим свойства, т.е. пару - ключ и значение.
Если нужно получить какое-то конкретное значение конкретного свойства, то 
мы обращаемся через точку. Т.е. переменная(обьект) потм точка потом ключ (имя) свойства.

*/